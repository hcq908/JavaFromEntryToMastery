public class PrintDiamond {
    public static void main(String args[]){
        for(int x=0;x<20;x++){
            System.out.println(x);
            System.out.println("  *");
            System.out.println(" ***");
            System.out.println("*****");
            System.out.println(" ***");
            System.out.println("  *");
        }
    }
}
